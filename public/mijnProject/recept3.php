        <h3> instructies </h3>
        <p> 1.	Week de blaadjes gelatine 5 min. in een kom met ruim, koud water. Laat het bladerdeeg ontdooien. Pers de limoen uit. Doe het sap met de helft van de aardbeien en de helft van de fijne kristalsuiker in de blender en pureer. Klop de slagroom stijf met de rest van de fijne kristalsuiker. Schep de aardbeienpuree voorzichtig door de slagroom. Verhit het water in een steelpan. Knijp de gelatine uit en los op in het water. Schep de opgeloste gelatine door het aardbeien-slagroommengsel en laat in de koelkast minimaal 4 uur opstijven. </p>

<p>2.	Verwarm ondertussen de oven voor op 180 °C. </p>
<p>3.	Halveer de plakjes bladerdeeg en leg ze op 2 met bakpapier beklede bakplaten. Prik met een vork gaatjes in het deeg en strooi er de suiker over. Bak de plakjes ca. 20 min. in de oven. Verwissel na 10 min. de bakplaten. Neem uit de oven en laat afkoelen. Leg 10 deegplakjes met de gesuikerde kant naar boven op het werkblad. Schep de aardbeienvulling in een spuitzak met een gladde spuitmond. Spuit een laag aardbeienvulling van 3 cm hoog op elk deegplakje. Halveer de rest van de aardbeien en duw ze met de snijkant naar buiten in de aardbeienvulling.</p>
<p>4.	Bestrijk de ongesuikerde kant van de rest van de plakjes bladerdeeg met de jam. Leg ze met de jamkant naar boven op de aardbeienvulling. Kneed op een werkblad de rolfondant met de kleurstof tot een marmereffect ontstaat. Rol met de deegroller uit op een met poedersuiker bestoven werkblad tot een plak van 10 x 40 cm. Snijd dat in 10 gelijke rechthoekjes en leg op elke tompouce een plakje. </p>
