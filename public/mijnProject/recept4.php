        <h3> instructies </h3>
        <p> 1.	Verwarm de oven voor op 175 °C. Bekleed de bodem van de springvorm met bakpapier en vet de randen in met boter. Zeef het bakmeel, de cacao en het zout in een kom. Hak de pure chocolade grof. </p>

<p>2.	Doe de suiker, boter, de helft van de pure chocolade, de gecondenseerde melk en kokend water in een pan. Verwarm al roerend op laag vuur tot een gladde massa. Voeg toe aan het bakmeel in de kom en roer tot een stevig geheel. Klop de eieren los, voeg ze toe en roer door. Schenk het mengsel in de springvorm en bak ca. 55 min. in het midden van de oven. </p>
<p>3.	Hak ondertussen de melkchocolade fijn en doe het met de rest van de pure chocolade in een kom. Controleer de gaarheid door een satéprikker in het midden van de taart te steken. Is hij droog dan is de taart gaar. Verhit de slagroom tot tegen de kook aan, schenk in de kom en meng al roerend tot de chocolade gesmolten is. Klop de chocolade met de mixer tot een glanzende massa en laat 30 min. op kamertemperatuur iets opstijven.</p>
<p>4.	Neem de taart uit de oven en laat 30 min. afkoelen in de vorm. Verwijder de vorm en laat in ca. 1.5 uur volledig afkoelen. Snijd de taart horizontaal doormidden. Bestrijk de bovenkant en beide helften met het chocolademengsel. Leg de helften weer op elkaar. </p>
